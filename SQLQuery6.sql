SELECT 
  ROUND(
    ABS(p1.LAT_N - p2.LAT_N) + ABS(p1.LONG_W - p2.LONG_W), 4
  ) AS manhattan_distance
FROM
  (SELECT MIN(LAT_N) AS LAT_N, MIN(LONG_W) AS LONG_W FROM STATION) AS p1,
  (SELECT MAX(LAT_N) AS LAT_N, MAX(LONG_W) AS LONG_W FROM STATION) AS p2;


